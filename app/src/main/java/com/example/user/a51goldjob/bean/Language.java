package com.example.user.a51goldjob.bean;

public class Language {

	private String languageid;
	private String type;
	private String mastery;
	private String randw;
	private String lands;
	public String getLanguageid() {
		return languageid;
	}
	public void setLanguageid(String languageid) {
		this.languageid = languageid;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getMastery() {
		return mastery;
	}
	public void setMastery(String mastery) {
		this.mastery = mastery;
	}
	public String getRandw() {
		return randw;
	}
	public void setRandw(String randw) {
		this.randw = randw;
	}
	public String getLands() {
		return lands;
	}
	public void setLands(String lands) {
		this.lands = lands;
	}
}
